                    if (!data.element) {
                        return data.text;
                    }

                    var $element = $(data.element);

                    var $wrapper = $('<div></div>');
                    $wrapper.addClass($element[0].className);

                    $wrapper.text(data.text);

                    return $wrapper;
                }
            });

            //read-only?
            if (typeof attr.readonly !== 'undefined' && attr.readonly == true) {
                select.select2("readonly", true);
            }

            return select;
        }


